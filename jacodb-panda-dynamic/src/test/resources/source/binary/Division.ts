function divideByConstant(number: number): number {
    return number / 5;
}

function divideConstantByNumber(number: number): number {
    return 5 / number;
}

function divideNumbers(a: number, b: number): number {
    return a / b;
}

function divideNumbersReversed(a: number, b: number): number {
    return b / a;
}

function divideByOne(number: number): number {
    return number / 1;
}

function divideOneByNumber(number: number): number {
    return 1 / number;
}

function divideMixedNumbers(a: number, b: number): number {
    return -a / -b;
}
