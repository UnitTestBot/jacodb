function subtraction(num: number): number {
    return num - 5;
}

function subtractReversed(num: number): number {
    return 5 - num;
}

function subtractNumbers(a: number, b: number): number {
    return a - b;
}

function subtractNumbersReversed(a: number, b: number): number {
    return b - a;
}

function subtractFromZero(number: number): number {
    return 0 - number;
}

function subtractToZero(number: number): number {
    return number - 0;
}

function subtractMixedNumbers(a: number, b: number): number {
    return -a - b;
}
