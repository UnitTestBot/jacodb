package org.usvm.jacodb.gen

import java.io.BufferedReader
import org.jacodb.go.api.*
class token_File {

	var name: String? = null
	var base: Long? = null
	var size: Long? = null
	var mutex: sync_Mutex? = null
	var lines: List<Long>? = null
	var infos: List<token_lineInfo>? = null
}

fun read_token_File(buffReader: BufferedReader, id: Int): token_File {
	val res = token_File()
    if (id != -1) {
        if (ptrMap.containsKey(id)) {
            return ptrMap[id] as token_File
        }
        ptrMap[id] = res
		structToPtrMap[res] = id
    }
    var line: String
    var split: List<String>
    var id: Int
    var readType: String

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.name = mapDec[readType]?.invoke(buffReader, id) as String?

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.base = mapDec[readType]?.invoke(buffReader, id) as Long?

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.size = mapDec[readType]?.invoke(buffReader, id) as Long?

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.mutex = mapDec[readType]?.invoke(buffReader, id) as sync_Mutex?

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.lines = mapDec[readType]?.invoke(buffReader, id) as List<Long>?

	line = buffReader.readLine()
	if (line == "end") {
        return res
    }
    split = line.split(" ")
    readType = split[1]
    id = -1
    if (split.size > 2) {
        id = split[2].toInt()
    }
    res.infos = mapDec[readType]?.invoke(buffReader, id) as List<token_lineInfo>?

	buffReader.readLine()
	return res
}
